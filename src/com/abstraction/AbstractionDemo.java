package com.abstraction;

public class AbstractionDemo {
  public static void main(String[] args) {
	int i=10;
	int j=20;
	//in this + operator is performing addition operation for primitive variables
	System.out.println(i+j);
	
	String s1="10";
	String s2 ="20";
	//in this + operator is performing concatenation operation for String variables
	System.out.println(s1+s2);
	
 }
	
	
}
